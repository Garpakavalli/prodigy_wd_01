// Select the navbar element
const navbar = document.querySelector('.navbar');

// Add an event listener to the window to detect scroll events
window.addEventListener('scroll', () => {
  // Check if the page has been scrolled down by more than 50 pixels
  if (window.scrollY > 50) {
    // Add the 'scrolled' class to the navbar to change its background color
    navbar.classList.add('scrolled');
  } else {
    // Remove the 'scrolled' class when back at the top of the page
    navbar.classList.remove('scrolled');
  }
});
